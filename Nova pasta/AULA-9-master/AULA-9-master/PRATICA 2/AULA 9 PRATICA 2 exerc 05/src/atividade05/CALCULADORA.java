public class CALCULADORA {
    public static int soma(int x, int y) {
        
        int soma = x + y;
        return soma;
    }
    public static int sub(int x, int y){
        
        int sub = x-y;
        return sub;
    }
    public static int mult(int x, int y){
        
        int mult = x*y;
        return mult;
    }
    public static float div(int x, int y){
        
        float div = (float)x/y;
        return div;
    }
    
}
