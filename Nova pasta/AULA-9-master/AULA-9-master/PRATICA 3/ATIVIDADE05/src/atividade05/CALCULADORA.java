public class CALCULADORA {
    public int soma(int x, int y) {
        int soma;
        soma = x + y;
        return soma;
    }
    public int sub(int x, int y){
        int sub;
        sub = x-y;
        return sub;
    }
    public int mult(int x, int y){
        int mult;
        mult = x*y;
        return mult;
    }
    public float div(float x, float y){
        float div;
        div = x/y;
        return div;
    }
    
}
